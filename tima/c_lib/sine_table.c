int acos_table( int val )
{
	int i;
	int32_t * cosine_table = ( int32_t * )&Sine_table[1024];
	if( val > 32768 ) return 0;
	for( i = 0; i < 2048; i++ )
	{
		if( val > cosine_table[i] ) return i;
	}

	return 0;
}
